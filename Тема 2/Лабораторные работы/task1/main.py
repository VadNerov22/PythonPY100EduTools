list_ = [8, 9, -5, -3, 1, -10, 8, -10, -5, 0, 5, -4, 0, 10, -8, 1, 6, -6, 6, -9]
# найти сумму, количество и среднее арифметическое уникальных чисел
summa_ = sum(set(list_)) # сумма уникальных чисел
count_ = len(set(list_)) # количество уникальных чисел
midle_ = summa_ / count_ # среднее арифметическое уникальных чисел
print(summa_)
print(count_)
print(round(midle_, 5))
